macro_rules! help {
	($args:expr) => {{
		println!("{} day part inputs\n\tday\t01..25\n\tpart = 1 or 2", $args[0]);
		println!("\tinputs: ");
		return
	}}
}

pub fn part1(args: Vec<String>) {
    println!("--- Day X: TBD ---");
    println!("--- Part 1");
    if args.len() != 4 { help!(args); }

}

pub fn part2(args: Vec<String>) {
    println!("--- Day X: TBD ---");
    println!("--- Part 2");
    if args.len() != 4 { help!(args); }

}
